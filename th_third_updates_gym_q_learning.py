def los_to_ground(satellite_position, ground_position, R_E):#changed to line of ######################################################################################sight bellow cuz its more accurate
      # get pos (x, y, z) for sat and gp
      x_s, y_s, z_s = satellite_position
      x_g, y_g, z_g = ground_position

      # vec  gr pt to sat
      v_gs = np.array([x_s - x_g, y_s - y_g, z_s - z_g])

      # vec  Earth cent to gr pt
      r_g = np.array([x_g, y_g, z_g])

      # dot v_gs and r_g
      dot_product = np.dot(v_gs, r_g)

      # mag of v_gs and r_g
      norm_v_gs = np.linalg.norm(v_gs)
      norm_r_g = np.linalg.norm(r_g)

      # get cos of  between v_gs and r_g
      cos_theta = dot_product / (norm_v_gs * norm_r_g)

      # If cos(theta) > 0, sat is in LoS (above horizon)
      if cos_theta > 0:
        return True  # sat  above the horizon
      else:
        return False  # sat  below  horizon (no LoS)

