        new_state = self.transition_probability(self.state, action)
        reward = self.compute_reward(self.state, action, new_state)

        self.state = new_state

        done = False
        if reward < -1000:  # Example cond to end episode
            done = True

        return self.state_space.index(self.state), reward, done, {}

