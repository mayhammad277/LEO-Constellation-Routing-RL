# Function to convert geodetic (lat, long, alt) to ECI (x, y, z)
def geodetic_to_eci_sky(lat, lon, alt, ts):
    earth = load('de421.bsp')['earth']
    location = Topos(latitude_degrees=lat, longitude_degrees=lon, elevation_m=alt * 1000)
    t = ts.now()
    position = (earth + location).at(t).position.km
    return np.array(position)

